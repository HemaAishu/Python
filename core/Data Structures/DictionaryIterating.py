dict1={1:"ganesh",2:"abi",3:"sara",4:"anu"}
# Using key
for key in dict1:
    print(key) # Output : 1 2 3 4
# Using Values
for value in dict1.values():
    print(value) # Output : ganesh abi sara anu
# Using key-value pairs
for items in dict1.items():
    print(items) # Output : (1, 'ganesh') (2, 'abi') (3, 'sara') (4, 'anu')