tup= tuple([1,2,5,8,4,'Sara Murthy',True])
tup1= (100,200)
# Concatenation
print(tup + tup1) # Output : (1, 2, 5, 8, 4, 'Sara Murthy', True, 100, 200)
# Repetition
print(tup1 * 2) # Output : (100, 200, 100, 200)
# Membership Testing
print(len(tup1)) # Output : 2
# Length
print( 8 in tup) # Output : True
print(100 not in tup1) # Output : False
